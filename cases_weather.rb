weather_today = "variable"

case weather_today 
when "sunny"
puts "Yeah, wear light clothes!"
when "rainy"
puts "cove more and take your umbrella"
when "cool"
puts "grab warm clothes"
when "foggy"
puts "drive carefully"
when "snowing"
puts "nice but pay attention"
else puts "not sure what type of weather this is"
end

